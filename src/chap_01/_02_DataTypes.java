package chap_01;

public class _02_DataTypes {
    public static void main(String[] args) {
        System.out.println("HelloWorld");//문자열
        System.out.println("안녕하세요?");//문자열
        System.out.println(12);//숫자 자료형
        System.out.println(-34);//숫자 자료형
        System.out.println(3.14);//숫자 자료형
        System.out.println(true);//boolean 자료형(참)
        System.out.println(false);//boolean 자료형(거짓)

        System.out.println(123 + 345);




    }
}
